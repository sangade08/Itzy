/**
 * Function to say "Happy Birthday".
 *
 * @param {string} name - The name of the person whose birthday it is.
 * @returns {string} A birthday message for the person.
 */
function sayHappyBirthday(name) {
    return `Happy Birthday, ${name}!`;
}
 
// Usage Example
const birthdayMessage = sayHappyBirthday("John");
console.log(birthdayMessage);  // Output: Happy Birthday, John!